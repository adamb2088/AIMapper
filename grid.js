function createGridFromArray(array) {
    const gridContainer = document.getElementById("grid-container");
    gridContainer.innerHTML = "";
    array.forEach((num, index) => {
        const gridItem = document.createElement("div");
        gridItem.classList.add("grid-item");
        gridItem.addEventListener("click", () => {
            if (num === 1) {
                if (redSquareIndex === null) {
                    array[index] = 3;
                    redSquareIndex = index;
                    redSquareX = index % 116;
                    redSquareY = Math.floor(index / 116);
                } else if (blueSquareIndex === null) {
                    array[index] = 4;
                    blueSquareIndex = index;
                    blueSquareX = index % 116;
                    blueSquareY = Math.floor(index / 116);
                } else {
                    array[redSquareIndex] = 1;
                    array[blueSquareIndex] = 1;
                    array[index] = 3;
                    redSquareIndex = index;
                    redSquareX = index % 116;
                    redSquareY = Math.floor(index / 116);
                    blueSquareIndex = null;
                    blueSquareX = null;
                    blueSquareY = null;
                }
            } else if (num === 3) {
                array[index] = 4;
                blueSquareIndex = index;
                blueSquareX = index % 116;
                blueSquareY = Math.floor(index / 116);
                array[redSquareIndex] = 1;
                redSquareIndex = null;
                redSquareX = null;
                redSquareY = null;
            } else if (num === 4) {
                array[index] = 3;
                redSquareIndex = index;
                redSquareX = index % 116;
                redSquareY = Math.floor(index / 116);
                array[blueSquareIndex] = 1;
                blueSquareIndex = null;
                blueSquareX = null;
                blueSquareY = null;
            }
            createGridFromArray(array);
            console.log("start point:", "X:", redSquareX, "Y:", redSquareY);
            console.log("Destination:", "X:", blueSquareX, "Y:", blueSquareY);
        });
        if (num === 1) {
            gridItem.style.backgroundColor = "white";
        } else if (num === 3) {
            gridItem.style.backgroundColor = "red";
        } else if (num === 4) {
            gridItem.style.backgroundColor = "blue";
        }
        gridContainer.appendChild(gridItem);
    });
}

