    function findPath(maze, startRow, startCol, endRow, endCol, visited) {
        // Check if we've reached the end and it's a valid space
        if (
            startRow === endRow &&
            startCol === endCol &&
            maze[startRow][startCol] === 1
        ) {
            return [[startRow, startCol]];
        }

        // Mark current cell as visited
        visited[startRow][startCol] = true;

        // Explore all four directions (up, down, left, right)
        const directions = [
            [0, 1],
            [0, -1],
            [1, 0],
            [-1, 0],
        ];
        for (const [dx, dy] of directions) {
            const newRow = startRow + dy;
            const newCol = startCol + dx;

            // Check if new position is within bounds, not a wall, and not visited
            if (
                newRow >= 0 &&
                newRow < maze.length &&
                newCol >= 0 &&
                newCol < maze[0].length &&
                maze[newRow][newCol] === 1 &&
                !visited[newRow][newCol]
            ) {
                // Try exploring from the new position
                const path = findPath(
                    maze,
                    newRow,
                    newCol,
                    endRow,
                    endCol,
                    visited
                );

                // If a path is found, prepend the current cell and return
                if (path) {
                    return [[startRow, startCol], ...path];
                }
            }
        }

        // Backtrack if no path is found from this cell
        visited[startRow][startCol] = false;
        return null;
    }

