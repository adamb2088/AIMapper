class MazeSolver {
    constructor(maze) {
        this.maze = maze;
        this.rows = maze.length;
        this.cols = maze[0].length;
        this.visited = new Array(this.rows)
            .fill(false)
            .map(() => new Array(this.cols).fill(false));
        this.directions = [
            [1, 0],
            [0, 1],
            [-1, 0],
            [0, -1],
        ]; // down, right, up, left
    }

    solveMaze(startX, startY, endX, endY) {
        const stack = [[startX, startY]];
        const path = [];

        while (stack.length > 0) {
            const [x, y] = stack.pop();
            if (x === endX && y === endY) {
                path.push([x, y]);
                console.log("solved");
                return path;
            }

            if (this.isValidMove(x, y)) {
                this.visited[x][y] = true;
                path.push([x, y]);

                for (let [dx, dy] of this.directions) {
                    const newX = x + dx;
                    const newY = y + dy;
                    if (this.isValidMove(newX, newY)) {
                        stack.push([newX, newY]);
                    }
                }
            }
        }

        return [];
    }

    isValidMove(x, y) {
        return (
            x >= 0 &&
            x < this.rows &&
            y >= 0 &&
            y < this.cols &&
            this.maze[x][y] === 1 &&
            !this.visited[x][y]
        );
    }
}

